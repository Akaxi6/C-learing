/*百鸡问题：公鸡每只5元，母鸡每只3元，小鸡3只1元。
用100元买100只鸡，问公鸡、母鸡和小鸡各能买多少只？
**要求输入提示信息为：无输入提示信息和输入数据
**输出格式要求为："x=%d,y=%d,z=%d\n"，
其中x,y,z分别表示公鸡、母鸡和小鸡只数。*/

#include<stdio.h>
int main()
{
	int g,m,x;
	for(g=0;g<=20;g++)
	{
		for(m=0;m<=33;m++)
		{
			for(x=0;x<=300;x++)
			{
				if(g+m+x==100&&5*g+3*m+x/3==100&&x%3==0)
				printf("x=%d,y=%d,z=%d\n",g,m,x);
			}
		}
	}
	return 0;
}
